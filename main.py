def get_card_image_path(card: dict):
    slug = CARD_IMAGE_SLUGS.get(card["name"])
    if not slug:
        return None

    for ext in ("jpg", "jpeg", "png", "webp"):
        path = f"cards/{slug}.{ext}"
        if os.path.exists(path):
            return path

    return None

async def send_card_images(message: Message, cards: list[dict]):
    """
    Optional visual tarot layer.
    Put images into ./cards/ using names from CARD_IMAGE_SLUGS:
    cards/fool.jpg, cards/magician.jpg, etc.
    If images are missing, the bot simply skips this step.
    """
    image_paths = [get_card_image_path(card) for card in cards]
    image_paths = [path for path in image_paths if path]

    if not image_paths:
        return

    if len(image_paths) == 1:
        await message.answer_photo(FSInputFile(image_paths[0]))
        return

    media = [InputMediaPhoto(media=FSInputFile(path)) for path in image_paths[:10]]
    await message.answer_media_group(media)


async def send_reading(message: Message, text: str, cards: list[dict], reply_markup=None):
    await send_card_images(message, cards)
    await send_text(message, text, reply_markup=reply_markup)


async def send_text(message: Message, text: str, reply_markup=None):
    text = cleanup_generated_text(text)
    chunks = split_long_text(text)

    for index, chunk in enumerate(chunks):
        await message.answer(
            chunk,
            reply_markup=reply_markup if index == len(chunks) - 1 else None,
        )



