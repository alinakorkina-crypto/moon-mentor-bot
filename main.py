def infer_question_mode(user_question: str) -> str:
    q = user_question.lower().replace("ё", "е")
    if any(word in q for word in ["ежик", "колюч", "закрыт", "холодн", "резк", "дистанц"]):
        return "hedgehog"
    if any(phrase in q for phrase in ["хочет быть", "быть со мной", "серьезно", "отношения со мной"]):
        return "commitment"
    if any(word in q for word in ["чувств", "любит", "нравлю", "интерес"]):
        return "feelings"
    if any(word in q for word in ["почему", "зачем"]):
        return "why"
    return "general"

def extract_spread_themes(cards: list[dict]) -> list[str]:
    counts = {}
    for card in cards:
        for theme in CARD_THEMES.get(card["name"], set()):
            counts[theme] = counts.get(theme, 0) + 1

    ordered = sorted(
        counts,
        key=lambda theme: (-counts[theme], THEME_PRIORITY.index(theme) if theme in THEME_PRIORITY else 999, theme),
    )
    return ordered[:4]

