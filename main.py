def split_long_text(text: str, chunk_size: int = 3800):
    chunks = []
    while len(text) > chunk_size:
        split_at = text.rfind("\n\n", 0, chunk_size)
        if split_at == -1:
            split_at = chunk_size
        chunks.append(text[:split_at])
        text = text[split_at:].strip()
    if text:
        chunks.append(text)
    return chunks


