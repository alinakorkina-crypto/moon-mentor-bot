def detect_llm_language(text: str) -> list[str]:
    """
    Safe detector. Does not crash if older v12 constants are absent.
    """
    found = []
    low = (text or "").lower()
    phrases = globals().get("BANNED_LLM_PHRASES", [])
    for phrase in phrases:
        if phrase.lower() in low:
            found.append(phrase)
    return found



def remove_llm_language(text: str) -> str:
    """
    Safe cleanup. Does not crash if older v12 constants are absent.
    """
    result = text or ""
    replacements = globals().get("LLM_PHRASE_REPLACEMENTS", {})
    for old, new in replacements.items():
        result = re.sub(re.escape(old), new, result, flags=re.IGNORECASE)
    return result



