def make_v12_observation_answer(cards: list[dict], user_question: str) -> str:
    mode = infer_question_mode(user_question)
    observations = extract_observations(cards)

    intro = build_observation_intro(cards, mode, observations)
    story, insight, question = build_observation_story(cards, mode, observations)

