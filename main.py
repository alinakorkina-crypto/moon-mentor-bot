def get_period_counts(analytics: dict) -> tuple[int, int]:
    today = get_today()
    days = analytics.get("days", {})
    today_count = days.get(today, 0)

    sorted_days = sorted(days.items(), key=lambda item: item[0], reverse=True)
    week_count = sum(count for _, count in sorted_days[:7])

    return today_count, week_count


def get_user_activity_counts(analytics: dict) -> tuple[int, int]:
    today = get_today()
    users = analytics.get("users", {})

    new_today = 0
    returned_today = 0

    for data in users.values():
        first_seen = str(data.get("first_seen", ""))[:10]
        last_seen = str(data.get("last_seen", ""))[:10]
        readings = int(data.get("readings", 0) or 0)

        if first_seen == today:
            new_today += 1
        elif last_seen == today and readings > 1:
            returned_today += 1

    return new_today, returned_today


