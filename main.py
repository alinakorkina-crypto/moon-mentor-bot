def update_analytics(record: dict):
    analytics = load_analytics()

    user_id = str(record["user_id"])
    analytics.setdefault("users", {})
    analytics["users"].setdefault(user_id, {
        "first_seen": record["date"],
        "last_seen": record["date"],
        "username": record.get("username"),
        "readings": 0,
    })
    analytics["users"][user_id]["last_seen"] = record["date"]
    analytics["users"][user_id]["username"] = record.get("username")
    analytics["users"][user_id]["readings"] += 1

    analytics["total_readings"] = analytics.get("total_readings", 0) + 1

    for key_name, value in [
        ("types", record.get("type")),
        ("days", record["date"][:10]),
        ("bot_versions", record.get("bot_version", BOT_VERSION)),
    ]:
        analytics.setdefault(key_name, {})
        analytics[key_name][value] = analytics[key_name].get(value, 0) + 1

    question = normalize_question(record.get("question"))
    if question:
        analytics.setdefault("questions", {})
        analytics["questions"][question] = analytics["questions"].get(question, 0) + 1

    analytics.setdefault("cards", {})
    for card in record.get("cards", []):
        analytics["cards"][card] = analytics["cards"].get(card, 0) + 1

    write_json_file(ANALYTICS_FILE, analytics)


def save_history(
    user_id: int,
    spread_type: str,
    cards,
    question: str | None = None,
    username: str | None = None,
    answer_length: int | None = None,
):
    history = load_history()
    user_key = str(user_id)
    now = datetime.now().strftime("%Y-%m-%d %H:%M")

    if user_key not in history:
        history[user_key] = []

    record = {
        "date": now,
        "type": spread_type,
        "cards": [card["name"] for card in cards],
        "bot_version": BOT_VERSION,
    }

    clean_question = normalize_question(question)
    if clean_question:
        record["question"] = clean_question

    if username:
        record["username"] = username

    if answer_length is not None:
        record["answer_length"] = answer_length

    history[user_key].append(record)
    history[user_key] = history[user_key][-100:]

    write_json_file(HISTORY_FILE, history)

    analytics_record = {
        **record,
        "user_id": user_id,
    }
    update_analytics(analytics_record)
    append_dialog_log(analytics_record)


