def cleanup_generated_text(text: str) -> str:
    """
    Technical cleanup only:
    - keeps Moon's style;
    - fixes broken fragments after validation/rewrite;
    - adds readable spacing after section headings.
    """
    if not text:
        return text

    result = text.strip()

    # Remove common broken sentence tails after validator cuts.
    broken_patterns = [
        r",\s*и\s*\.",
        r",\s*но\s*\.",
        r",\s*а\s*\.",
        r",\s*что\s*\.",
        r",\s*\.",
        r"\s+и\s*\.",
        r"\s+но\s*\.",
        r"\s+а\s*\.",
        r"\s+что\s*\.",
        r":\s*\.",
        r"\(\s*\)",
    ]
    for pattern in broken_patterns:
        result = re.sub(pattern, ".", result, flags=re.IGNORECASE)

    # Fix frequent grammar artefacts from rewrite.
    fixes = {
        "Привычный общение": "Привычное общение",
        "привычный общение": "привычное общение",
        "твоё общение": "ваше общение",
        "в твоих отношениях": "в ваших отношениях",
        "один из тебя": "один из вас",
        "одна из тебя": "одна из вас",
        "инициатиты": "инициативы",
        "тыгляд": "выгляд",
    }
    for old, new in fixes.items():
        result = result.replace(old, new)

    # Ensure section headings have blank line after them.
    headings = [
        "🃏 Что привлекло внимание в раскладе",
        "🌙 Как это может выглядеть со стороны",
        "✨ Что кажется самым важным",
        "💭 Вопрос для размышления",
        "🕯 На что посмотреть",
        "🌙 Что видно сейчас",
        "💭 Вопрос себе",
    ]

    for heading in headings:
        result = re.sub(
            rf"{re.escape(heading)}\s*(?!\n\n)",
            heading + "\n\n",
            result,
        )

    # Ensure each heading starts on a new paragraph if it was glued to previous text.
    for heading in headings:
        result = re.sub(
            rf"([^\n])\s*{re.escape(heading)}",
            rf"\1\n\n{heading}",
            result,
        )

    # Remove editorial/Markdown artefacts from LLM polishing.
    result = re.sub(r"^\s*Вот доработанный вариант:\s*", "", result, flags=re.IGNORECASE)
    result = re.sub(r"^\s*Вот вариант:\s*", "", result, flags=re.IGNORECASE)
    result = result.replace("**", "")

    # Clean duplicated punctuation and spaces.
    result = re.sub(r"\.{2,}", ".", result)
    result = re.sub(r"\s+([,.!?])", r"\1", result)
    result = re.sub(r"[ \t]{2,}", " ", result)
    result = re.sub(r"\n{3,}", "\n\n", result)

    return result.strip()



def humanize_moon_text(text: str) -> str:
    result = text

    for old, new in HUMANIZE_REPLACEMENTS:
        result = re.sub(re.escape(old), new, result, flags=re.IGNORECASE)

    # Убираем случайные приветствия модели.
    result = re.sub(r"^\s*Привет[!.]?\s*", "", result, flags=re.IGNORECASE)
    result = re.sub(r"^\s*Давай посмотрим[^\\n]*\n*", "", result, flags=re.IGNORECASE)
    result = re.sub(r"^\s*Что там интересного[^\\n]*\n*", "", result, flags=re.IGNORECASE)

    result = re.sub(r"\bв данной ситуации\b", "здесь", result, flags=re.IGNORECASE)
    result = re.sub(r"\bв настоящий момент\b", "сейчас", result, flags=re.IGNORECASE)
    result = re.sub(r"\bкаким образом\b", "как", result, flags=re.IGNORECASE)

    # Чистим частые опечатки после старых версий humanize.
    result = result.replace("тыгляд", "выгляд")
    result = result.replace("инициатиты", "инициативы")

    result = re.sub(r"\n{3,}", "\n\n", result)
    return cleanup_generated_text(result)



